# end = ' ' is used to have the input to the right
# of the text. Instead of a new line.
print("How old are you?", end = ' ')
age = input()
print("How tall are you?", end = ' ')
height = input()
print("How much do you weigh?", end = ' ')
weight = input()

print(f"So, you're {age} years old, {height} feet tall, and {weight} pounds heavy.")

